/*MONDAY*/
  function monday() {
    var x = document.getElementById("onMon1");
    var y = document.getElementById("onMon2");
    if (x.style.display === "none" && y.style.display === "none") {
      x.style.display = "block";
      y.style.display = "block";
    } else {
        x.style.display = "none";
        y.style.display = "none";
      }
  }
